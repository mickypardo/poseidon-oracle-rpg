package org.mpardo.apps.model;

public class ChoosingTheEventFocus {
    
    private EventFocus eventFocus;

    private String Reason;

    public ChoosingTheEventFocus() {
    }

    public EventFocus getEventFocus() {
        return eventFocus;
    }

    public void setEventFocus(EventFocus eventFocus) {
        this.eventFocus = eventFocus;
    }

    public String getReason() {
        return Reason;
    }

    public void setReason(String reason) {
        Reason = reason;
    }

}
